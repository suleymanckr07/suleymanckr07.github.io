function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;

    // Update simple text elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update specific complex HTML updates if needed (e.g. hero title with spans)
    // Note: For now we stick to simple replacement. For preserving HTML formatting in future, we'd need innerHTML keys.
    if (lang === 'en') {
        document.querySelector('.hero h1').innerHTML = 'Make a Difference <br> <span class="text-gradient">in Mobile World</span>';
    } else {
        document.querySelector('.hero h1').innerHTML = 'Mobil Dünyasında <br> <span class="text-gradient">Fark Yaratın</span>';
    }

    // Explicitly update About Subtitle as it contains HTML
    const aboutSubtitle = document.querySelector('[data-i18n="about_subtitle"]');
    if (aboutSubtitle && translations[lang]['about_subtitle']) {
        aboutSubtitle.innerHTML = translations[lang]['about_subtitle'];
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'tr';
    const langBtn = document.getElementById('language-toggle');

    // Initial set
    if (savedLang === 'en') {
        setLanguage('en');
        if (langBtn) langBtn.textContent = 'TR';
    } else {
        // Default is usually already TR in HTML, but we run it to be safe or if we want to ensure consistency
        // setLanguage('tr'); 
        if (langBtn) langBtn.textContent = 'EN';
    }

    // Toggle Button Listener
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            const current = localStorage.getItem('language') || 'tr';
            const next = current === 'tr' ? 'en' : 'tr';
            setLanguage(next);
            langBtn.textContent = next === 'en' ? 'TR' : 'EN';
        });
    }
});
